package com.ford.avarsdl.exception;

public class ExtensionFileException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ExtensionFileException() {
		super();
	}

	public ExtensionFileException(String detailMessage, Throwable throwable) {
		super(detailMessage, throwable);
	}

	public ExtensionFileException(String detailMessage) {
		super(detailMessage);
	}

	public ExtensionFileException(Throwable throwable) {
		super(throwable);
	}

	
}
